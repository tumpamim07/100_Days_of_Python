fruits = ["Apple", "Peache", "Pear"]    # Creating a list that stores three fruit names
for fruit in fruits:            # Looping through each fruit inside the list one by one
    print(fruit)                # Showing the current fruit on the screen
    print(fruit + " pie")       # Showing the fruit name combined with the word "pie"
print(fruits)                   # Showing the entire original list after the loop is done